class Address:
    def __init__(self, address_id, customer_id, street, city, state, zip_code, address_type):
        self.address_id = address_id
        self.customer_id = customer_id
        self.street = street
        self.city = city
        self.state = state
        self.zip_code = zip_code
        self.address_type = address_type
